def multiplier(number1, number2):
    result = number1 * number2
    print(f"{number1} * {number2} = {result}")

number1_var = 20
number2_var = 15

multiplier(number1_var, number2_var)
multiplier(5,6)
multiplier(15 + 7, 21 - 13)
multiplier(number1_var * 5, number2_var / 3)
multiplier(20*20, 15*15)
